//4. Define variables for different Data Types int, Boolean, char, float, double and print on the Console

public class DataTypesInJava {
    public static void main(String[] args) {
        int i = 111;             
        float f = 11.1f;            
        double d = 111.1111;        
        char c = 'C';             
        boolean b = false;           

        System.out.println("This is an Integer "+i);
        System.out.println("This is a Float " + f);
        System.out.println("This is a Double " + d);
        System.out.println("This is a Char " + c);
        System.out.println("This is a Boolean " + b);
    }
}